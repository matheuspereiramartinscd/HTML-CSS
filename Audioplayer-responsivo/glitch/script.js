

var muaudio = "null";


function Redlight()
{
    muaudio = "audio/Red Light - Egor Buddenny.mp3";
    document.getElementById('audioplayer').src = muaudio;
    document.getElementById('stopicone').classList.add('hidden');
    document.getElementById('starticone').classList.remove('hidden');
    document.getElementById("status").innerHTML = "Pausado..";
    document.getElementById("musicname").innerHTML = "01. Red Light - Egor Buddenny"
    document.getElementById("status").style.color = "34F789";
    document.getElementById("musicname").style.color = "3EECED";
}

